🔹 To delete a specific row where id = 58:

DELETE FROM students
WHERE id = 58;
✅ This command will remove only the row from the students table where the id is 58.

⚠️ To drop (permanently delete) the entire table:

DROP TABLE students;
⚠️ Warning: This command will delete the entire students table, including all its data and structure. Once executed, it cannot be undone unless you restore from a backup.
